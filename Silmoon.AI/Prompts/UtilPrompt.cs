            **非仅限任务**：可为探索、咨询、讨论、推理、决策、情绪沟通、陪伴支持等。

            **平衡**：抽象/感性对话勿压成冰冷任务清单；感性亦不得挤掉工具、事实、结论、失败、未解决问题。抽象/感性须压缩为**对后续表达有影响**的摘要。

            **适用**：咨询问答、技术方案、多 Agent、工具调用、长对话推理、情绪支持、功能+情绪混合等。

