            - **进行中（Doing）**：（未收尾、仍占线的部分）
            - **待办/下一步（Next）**：（**仅**尚未做的；勿把 Done 再写进 Next；无则说明）
            - 已确认的重要上下文：

            ### [工作状态快照]（无则「无」）
            - 项目/仓库/路径/分支/环境（仅历史）：
            - 已触及文件、模块或交付物（仅历史）：
            - 阻塞或依赖（仅历史）：

            ---

            ### [沟通风格与情绪脉络（Interaction & Emotional Tone）]
            - 历史沟通基调：
            - 用户当前更需要：
            - 回答应保持的风格：
            - 需延续的感性/抽象支持（如有）：
            - 需保留的理性/功能性支持：

            ---

            ### [认知演化（Cognitive Evolution）]
            按时间顺序；每步：**类型** | **来源** | **内容** | **结果/影响** | **状态**（置信度）。

            - **Step 1**：类型… 来源… 内容… 结果… 状态…
            - **Step 2**：…

            ---

            ### [冲突与分歧]（如有）
            - 冲突点：…
              - 观点A（来源）：…
              - 观点B（来源）：…
              - 当前状态：…

            ---

            ### [工具与长内容（Artifacts）]
            - **Artifact 1**：名称/标识；类型（代码/数据/日志/网页/工具返回/长文本/情绪交流记录/其他）；来源；是否保留全文（是/否已压缩）；用途；关键内容摘要；关键结论；当前状态（有效/失败/存疑/过期/已替代）。
            - **Artifact 2**：…

            ---

            ### [已否定路径 / 错误尝试]
            - ...

            ### [当前未解决问题]
            - ...

            ### [续接指令（Continuation Guidance）]
            - 基于以上续接，非新对话；首轮可体现已读本包（不必冗长寒暄）。
            - **防重复执行（强制）**：续接后的助手须把 **Done** 视为**已发生且有效**，**禁止**为「验证」「保险」或「走完整流程」而再次执行相同命令、工具调用或重复劳动；**仅**推进 **Doing / Next** 中仍未落实的部分。若某步已在 Done 中，默认其产物（文件、配置、部署结果等）**仍存在**，除非记忆包标明已回滚/失效。
