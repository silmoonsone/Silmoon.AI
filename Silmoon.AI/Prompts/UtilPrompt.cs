        public static string ContinuationMemoryPrompt { get; set; } = """
            # 对话续接记忆包（Continuation Memory）

            ## 是什么 / 为什么（先读这段）

            你正在为即将被清空的对话历史，撰写一份**只读状态快照**，让下一轮模型读到它即可**续接**，而不必重放全部原始历史。

            **核心目的**：
            - 解决长对话下的上下文窗口溢出问题。
            - 降低 token 成本与网络传输压力。
            - 在大幅压缩的前提下，不丢失对续接有决定性影响的认知与状态。

            **性质**：
            - 它**替代**全部历史，不是普通摘要。
            - 清空后，本正文将成为新会话的**首条用户侧内容**。
            - 你的工作**止于写出本 Markdown 正文**（若宿主有提交工具则一并提交），**不要**去执行任何“下一步”。
            - 使用标准 Markdown（标题、列表、加粗、行内代码），不要使用任何模型专属语法。

            ---

            ## 撰写规则

            ### 1. 篇幅与取舍（最重要）
            - 目标是**替代**原历史，所以记忆包应**显著短于**原历史（经验上约为原历史 token 数的 10%~30%；越短越好，但以不丢失关键认知为前提）。
            - **保留 vs 丢弃判据**：
              - **保留**：**无法**由常识、通用知识或重新查询恢复的东西——用户明确表达过的偏好/禁忌、已做出的决策与理由、已确认的具体事实/数据、已放弃的路径与理由、情绪基调、多方观点与最终共识等。
              - **可丢**：能由续接模型凭常识或重新查询恢复的过程细节、反复寒暄、未产生结论的漫谈、中间冗余解释。
            - **禁止**为填满模板而扩写；**无内容的子项或整节直接省略**（不要写“无”占位，那是在浪费 token）。
            - **但**不得以“元事件”替代“实质内容”：例如“讨论了方案 A/B”必须说清 A/B 各是什么、最终选了哪个、为什么。

            ### 2. 真实性
            - 内容**仅**来自真实历史对话；**禁止**虚构路径、命令、输出、观点、结论。
            - 不确定的信息标注 `uncertain`，不要当成事实写。
            - 多方观点请**按来源归因**（`user` / `assistant` / `modelA` / 工具 等），不要合并成无主的“大家认为”。

            ### 3. 禁止“元任务循环”
            `Doing` / `Next` **只能**写实质业务（开发、排查、协商、情感陪伴、用户原本任务等）。

            **禁止**写入：
            - 再调用记忆 / 总结 / 重置类工具。
            - “验证续接记忆是否就绪”“再跑一遍记忆流程”等元流程。
            - 本记忆包自身的撰写、提交、校对。

            本包能写出即表示该元流程在旧对话中已闭合。如需提及，**仅**在 `Done` 写一条“已产出续接记忆包正文”。

            ### 4. 宿主工具（如启用了记忆提交类工具）
            - 全文**只出现一次**，放入工具参数（如 `continuation_memory`）。
            - **不要**在助手可见回复里再贴一次全文；留空或一句“已提交续接记忆”即可。

            ### 5. 元叙述最小化
            不展开写“我是怎么解析这条提示的/心路历程”。`Done` 里允许一条“已产出正文”作为例外。

            ---

            ## 输出模板

            > 原则：**有内容才写**；无内容的子项或整节**直接省略**。标题可合并。保持字段风格一致。

            ### 当前阶段
            一句话说明此刻处于对话的哪个阶段（新对话 / 推进中 / 收尾 / 闲聊中 / 等待用户下一步 等）。

            ### 对话性质（必填）
            - 主倾向：**任务向** | **闲聊/情感向** | **混合**
            - 依据：1–3 条历史线索
            - 首轮建议重心：一句话
            - 备注（可选）：如涉及多模型撮合、代码重构、长期陪伴等特定模式，可在此一句话标注

            ### 当前主题 / 用户关注点
            - …

            ### 关键状态（State）
            - **目标 / 方向**：…
            - **关键约束**：…
            - **Done**（已完成，续接模型**不必重做**）：…
            - **Doing**（进行中，仅实质业务）：…
            - **Next**（待办，仅实质业务）：…
            - **已确认的重要上下文**：…

            ### 核心内容要点（Substance，必填）

            > 这一节承载“**内容本身**”，是续接模型的第一依据。下列类别**按需列出**——有内容就写，没有就**整条省略**。不得用“已讨论过 / 已表达偏好 / 已达成共识”这类空话替代具体内容。

            - **决策与理由**：做出的选择及其依据（防止续接模型不明就里地推翻）。
            - **关键事实 / 数据 / 参数 / 产物要点**：可复现的具体内容（命令、版本、阈值、文件、URL 等）。
            - **用户画像点**：**据实而非推断**的偏好、禁忌、习惯、长期目标、性格或表达风格线索。
            - **情绪线索与未竟话题**：对闲聊/陪伴类尤为关键（基调、触发点、上次聊到哪里可以承接）。
            - **多方观点与共识**（若发生过意见交换/协商）：
              - 各方要点（**按来源**）
              - 主要分歧点
              - 共识如何达成（关键转折或决定性论据）
              - **最终共识内容**（非“已共识”空话）
              - 值得保留的少数派 / 备选观点
            - **已放弃的方案与理由**：避免续接模型重试。
            - **Artifacts 要点**（若有长文本/大块产物）：标识、用途、摘要、状态。

            ### 工作状态快照（编程/运维等具体工作才写）
            - 仓库 / 分支 / 路径 / 环境
            - 已触及文件或模块
            - 阻塞点（仅历史事实）

            ### 沟通风格与情绪脉络（任务纯粹、无情绪痕迹时可省略）
            - 用户基调
            - 期望的回答风格
            - 是否需要情感支持

            ### 认知演化（可选；简单对话可一句或省略）

            每步：`类型 | 来源 | 内容 | 结果/影响 | 置信度`

            - 类型：`Exploration` / `Insight` / `Decision` / `Failure` / `Tool` / `Task` / `EmotionalSupport` / `InteractionPattern`
            - 来源：`user` / `assistant` / `system` / `modelA` / `modelB` / `unknown`
            - 置信度：`confirmed` / `probable` / `uncertain` / `rejected`

            ### 冲突与分歧（仅在仍有未解分歧时写）
            观点A / 观点B / 来源 / 状态（未解决 / 倾向A / 保留分歧）

            ### 已否定路径 / 当前未解决问题（按需）
            - …

            ---

            ## 给下一轮模型的话（续接指令）

            > 这一节的受众是**读到本包的下一轮模型**，不是撰写者。请原样保留。

            **如果你读到的第一条用户消息就是本记忆包**：

            1. 历史**已经**完成压缩与重置，当前会话就是续接后的新上下文。
            2. **不要**再调用任何“总结 / 重置 / 压缩记忆”类工具，除非用户**明确**开口要求再来一次。
            3. **承接以「核心内容要点（Substance）」为第一依据**；`Done` / `认知演化` 只是索引。
            4. 把 `Done` 当作已发生的事实，**不要**为了验证而重复执行；产物（文件、配置、结论、用户已表达过的偏好、已达成的共识）默认仍然存在，除非本包明确写了回滚/失效。
            5. 只推进 `Doing` / `Next` 里的**实质**待办；若发现其中被误写了续接元任务，直接忽略。
            6. 多方协商场景：默认尊重「最终共识」；若用户再次召集意见，可基于保留的少数派观点重启，而不是从零开始。
            7. 首轮简短承接：任务向对齐未竟目标；闲聊向优先承接情绪与关系连续性；**不必冗长寒暄**。

