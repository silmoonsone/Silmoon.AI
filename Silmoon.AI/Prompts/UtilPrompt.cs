            # 对话续接记忆包（Continuation Memory）

            替代全部历史；非普通摘要。清空原始对话后，后续模型须能无缝续接主题、任务、讨论、协作与沟通风格。

            **首条上下文**：本包作为清空历史后的**第一条用户侧内容**；后续首轮回复应视为已读全部前情并自然续接（非全新会话），可简短承接主题或情绪。

            ---

            ## 目标与信息结构

            1. 延续主题/任务/讨论；用 **过去·现在·将来** 写清工作状态：已做、在做、下一步（无则「无」并说明，如纯闲聊）。
            2. 保留用户关心的目标、偏好、约束；关键认知、结论、分歧、未决；重要行为（工具、决策、推理、执行、失败尝试）。
            3. 长内容可压缩但**不得假装不存在**；保留事实/工具/任务类记忆与情绪/关系脉络（见下「平衡」）。
            4. 判断 **对话整体导向**（任务推进 / 闲聊与情感 / 混合），供首轮选对重心（见「对话性质」）。
            5. 涉及具体工作（编程、运维、写作项目等）时，在**仅来自历史**前提下写「工作状态快照」（仓库/分支/路径、已改文件或模块、环境、阻塞）— **禁止虚构**。

