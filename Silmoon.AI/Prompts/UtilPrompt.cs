using System;

namespace Silmoon.AI.Prompts
{
    public class UtilPrompt
    {
        public static string ContinuationMemoryPrompt { get; set; } = """
            # 对话续接记忆包（Continuation Memory）

            > 正文请使用 **Markdown**（标题、列表、加粗、行内代码）；结构清晰即可。

            **文档性质（撰写时必读）**  
            你是在编写一份**只读的记忆记录 / 状态快照**，用于在清空历史后交给**下一轮**模型阅读。**不是**在执行用户任务清单、**不是**要求你在写完本包后再去做 Done 里的动作、**不是**开启新一轮「续接流程」。Done/State 仅客观记录旧对话中已发生的事；撰写者的工作**止于产出本 Markdown 正文**（并通过宿主工具提交，若适用），**无需**在正文里给自己布置「下一步要再总结/再重置」之类的元任务。

            替代全部历史（非普通摘要）。清空后，后续模型应能续接主题、任务、情绪与风格。本包为清空后**首条用户侧内容**。

            ---

            ## 核心规则（必读）

            **篇幅**  
            与历史**信息量**成正比：短对话/单命令 → 短文；**禁止**为填满模板而扩写。**高密度**=单位字数信息量，非字数多。无内容则写「无」，勿套话占位。

            **宿主工具**  
            若用 `MemoryApplyTool`：**全文只出现一次**，放入参数 `continuation_memory`。**禁止**同轮在助手可见正文再贴一遍全文（可留空或一句「已提交续接记忆」）。

            **State 时间语义**  
            `Doing` / `Next` **仅**实质业务（开发、排查、用户任务等）。**禁止**写入：记忆工具链、`MemorySummarizeTool`/`MemoryApplyTool`、记忆包撰写/提交中、等元流程。**禁止**在 `Next` 写「验证续接记忆是否就绪 / 再跑一遍记忆流程」——本包能写出即表示该元流程在旧对话中已闭合。读者拿到正文时记忆包**已写成**；若须提元流程，**仅**在 `Done` 单条写「已按规范产出续接记忆包正文」，且不与 Doing/Next 重复。

            **真实性**  
            仅来自历史对话；**禁止**虚构路径/文件/执行结果。工作快照无则「无」。

            **元叙述**  
            不展开写解析提示词、心路；**禁止**在认知演化/Artifacts 长篇复述「如何写本包」。`Done` 允许单条「已产出正文」例外。

            ---

            ## 目标与覆盖

            1. **过去·现在·将来**：已做、在做、下一步（无则说明，纯闲聊可极简）。
            2. 保留：用户目标与约束、关键结论与分歧、重要行为（工具/决策/失败尝试）；长内容可压缩但**不得假装不存在**。
            3. **对话性质**：任务推进 / 闲聊情感 / 混合 → 见「对话性质」节，供首轮定调。
            4. 有编程/运维等具体工作时写「工作状态快照」，**仅历史事实**。

            **五类按需覆盖（勿凑数）**

            | 维度 | 要求 |
            |------|------|
            | State | 区分 Done/Doing/Next；无则「无」 |
            | 认知演化 | 可极简或一步 |
            | 行为脉络 | 可并入演化一句 |
            | Artifacts | 仅真实出现的长物/工具产物；无则「无」 |
            | 沟通 | 轻量对话可两三行 |

            **平衡**：抽象对话勿压成冰冷清单；任务向勿丢情绪线索；反之亦然——凡写入须**对续接有影响**。

            ---

            ## 记录规范

            **事件类型**（有则标；轻量可一句）：`Exploration` | `Insight` | `Decision` | `Failure` | `Tool` | `Task` | `EmotionalSupport` | `InteractionPattern`

            **来源**：`source: user | assistant | agent_x | system`，不可判则 `unknown`。

            **冲突**（若有）：观点A/B、来源、状态（未解决/倾向/已解决等）。

            **置信度**（重要结论）：`confirmed` | `probable` | `uncertain` | `rejected`。

            **Artifacts 条目**（有则写）：须说明用途；极长可标「已压缩仍依赖」。**勿**无则硬编 Artifact 列表。

            **情绪**：明显感性/陪伴时，在「沟通风格与情绪脉络」写基调与偏好（**仅据实**）。

            ---

            ## 输出格式（标题可合并、章节可无则一行「无」）

            ### [当前阶段]
            - 持续上下文（非新对话）

            ### [对话性质（Conversation Orientation）]（必填）
            - 主倾向：**任务/工作** | **闲聊/情感** | **混合**；依据 1～3 条；首轮建议重心

            ### [当前主题] / [用户关注点]
            - …

            ### [关键状态（State）]
            - 目标/方向；关键约束
            - **Done**：已落实项（命令、成功工具、已写文件等），**不必再重做**；续接正文已写就时可单列「已产出续接记忆包正文」
            - **Doing** / **Next**：**仅**实质业务；**勿**写续接元任务（规则见「核心规则」）
            - 已确认的重要上下文

            ### [工作状态快照]（无则「无」）
            - 仓库/分支/路径/环境；已触及文件或模块；阻塞（均仅历史）

            ---

            ### [沟通风格与情绪脉络]
            - 基调、用户更需要、回答风格、感性/理性支持（如有）

            ---

            ### [认知演化]
            - 每步：`类型 | 来源 | 内容 | 结果/影响 | 状态(置信度)`；简单对话可仅 Step 1 或「无多步演化」

            ---

            ### [冲突与分歧]（无则「无」）

            ### [工具与长内容（Artifacts）]（无则「无」）
            - 条目：标识、类型、来源、是否全文、用途、摘要、结论、状态

            ---

            ### [已否定路径] / [当前未解决问题]
            - …

            ### [续接指令（Continuation Guidance）]
            - **首条即本包时（强制）**：若用户第一条消息**就是**本记忆包（或带宿主前缀的同一正文），说明历史**已经**完成「压缩与重置」，当前会话**就是**续接后的新上下文。**禁止**再调用 `MemorySummarizeTool` / `MemoryApplyTool`、**禁止**再「总结一遍/重置一遍」——除非用户**明确口头**要求再次续接记忆。本段是**中断后的多轮信息压缩与状态恢复结果**，不是待你重新执行的流程说明。
            - 续接模型：把 **Done** 当已发生；**勿**为验证重复执行；**仅**推进 Doing/Next 中**实质**待办（先忽略误写的续接工具条目）。工具结论与关键事实勿无故丢弃（与简洁并存：用最少字保留要点）。
            - 产物默认仍存在，除非记忆包写明回滚/失效。
            - 任务向对齐未竟目标；闲聊向优先情绪与关系连续性；首轮可简短承接，不必冗长寒暄。

            ---

            ## 自检

            - 篇幅与历史体量匹配；Done/Next 无交叉；Doing/Next 无续接元任务。
            - 无模板灌水、无重复全文；最短必要信息可续接。
            - **[续接指令]** 已写明「首条即本包时勿再跑续接工具」；撰写者未把「再总结/再重置」写进 Next。
            """;

