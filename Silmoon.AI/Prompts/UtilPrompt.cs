            禁止空结构、占位、泛化句（如「进行了讨论」）；禁止以「无有效历史任务行为」偷懒。每条须对续接有用，否则删。

            无明确任务执行时仍须至少：主题、用户关注点、已形成的观点/结论/倾向、情绪与沟通特征（若有）。

            ---

            ## 记录规范

            **事件类型（每条须标注其一）**  
            Exploration（探索/分析/提问）；Insight（观点/结论/认知变化）；Decision（方案/方向/立场）；Failure（失败/否定路径）；Tool（工具/外部输入）；Task（编码/修改/执行）；EmotionalSupport（安抚/陪伴/共情）；InteractionPattern（风格/偏好/互动模式变化）。

            **来源**：每条须可溯—`source: user | assistant | agent_x | system`；仅当无法判断时用 `source: unknown`。

            **冲突**（若有）：冲突点；观点A（来源）；观点B（来源）；当前状态：未解决/倾向A/倾向B/已解决/暂缓。

            **置信度**（重要结论须标）：`confirmed` 已确认；`probable` 较可信未完全确认；`uncertain` 存疑；`rejected` 已否定或废弃。

            **情绪与沟通**（若存在明显感性/支持/陪伴）：在「沟通风格与情绪脉络」中写清：基调；理性/感性/结合；线索（如焦虑、犹豫、低落、求证、需理解或安慰、需冷静分析、需情理兼顾—**仅据实**）；偏好（直接/温和/结构化/陪伴型等）；延续—勿骤冷、勿无端煽情；混合则理性内容+感性承接。

            **Artifacts**：代码、日志、JSON/XML/YAML/配置、工具返回、网页/文档、长篇分析或长篇情绪文本等均须纳入。每项须含下方输出格式所列字段；须说明**起过什么作用**，禁止只写「存在对话」；很长仍重要者可标「已压缩但仍需依赖」。长物须保留：存在性、来源、用途、关键结论、状态（有效/失败/存疑/过期/已替代）。

            ---

            ## 输出格式（严格遵守）

