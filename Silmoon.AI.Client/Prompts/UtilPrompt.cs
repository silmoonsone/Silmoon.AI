using System;
using System.Collections.Generic;
using System.Text;

namespace Silmoon.AI.Client.Prompts
{
    public class UtilPrompt
    {
        public static string ContinuationMemoryPrompt { get; set; } = """
            # 你现在要生成“对话续接记忆包（Continuation Memory）”

            用于替代全部历史对话。

            这个记忆包的目的不是普通摘要，而是：在清空原始历史后，让后续模型仍然能够无缝继续当前对话、任务、讨论、协作与沟通风格。

            ---

            ## 核心目标

            在清空历史后，仍然能够：

            1. 延续当前主题、任务、问题或讨论方向
            2. 保留用户真正关心的问题、目标、偏好、约束
            3. 保留多轮对话中形成的关键认知、结论、分歧、未决事项
            4. 保留已经发生的重要行为，包括：
               - 工具调用
               - 决策
               - 推理
               - 任务执行
               - 失败尝试
            5. 对长内容进行压缩，但不能假装它不存在
            6. 如果历史中存在明显的情绪支持、陪伴、安抚、共情、鼓励、感性交流：
               - 必须保留这种沟通氛围与互动方式
               - 必须记录用户当时更需要“理性分析”还是“感性支持”或“两者结合”
               - 后续模型应延续这种沟通方式
            7. 同时不能丢失所有工具性、功能性、事实性、任务性记忆

            ---

            ## 适用范围

            此记忆包必须兼容以下场景：

            - 咨询 / 问答 / 知识讨论
            - 技术分析 / 方案设计 / 策略讨论
            - 多模型协作（Agent之间）
            - 工具调用型系统
            - 长对话 / 多轮推理
            - 情绪支持 / 感性沟通 / 关系延续
            - 同时包含“功能性内容 + 情绪性内容”的混合场景

            ---

            ## 核心原则

            1. 不强制必须是“任务”，可以是：
               - 探索
               - 咨询
               - 讨论
               - 推理
               - 决策
               - 情绪沟通
               - 陪伴支持

            2. 必须同时保留五类信息：
               - **状态（State）**：当前主题、目标、约束、进度
               - **认知演化（Cognitive Evolution）**：讨论过程、判断、结论、分歧
               - **行为记录（Actions）**：工具调用、执行行为、决策、失败路径
               - **长内容记录（Artifacts）**：代码、日志、数据、网页、长文本、长工具返回
               - **沟通风格与情绪脉络（Interaction & Emotional Tone）**：沟通方式、情感支持需求、关系状态

            3. 长内容必须保留：
               - 存在性
               - 来源
               - 用途
               - 关键结论
               - 当前状态（有效 / 失败 / 存疑 / 过期 / 已替代）

            4. 必须区分“谁提出了什么”：
               - 用户
               - assistant
               - agent_x
               - system

            5. 如果历史中有较多抽象化、哲学化、感性化、关系导向的对话，不得把它们全部压缩成冰冷的任务状态。必须保留：
               - 交流基调
               - 用户关注的情感层面
               - 已建立的互动风格
               - 对后续表达方式有影响的抽象结论

            6. 但感性内容不能覆盖或挤掉：
               - 工具性记忆
               - 功能性记忆
               - 关键事实
               - 已确认结论
               - 失败经验
               - 未解决问题

            ---

            ## 强制约束（防错误）

            1. 严禁记录以下内容：
               - 本次生成记忆包的行为
               - 解析提示词
               - 当前推理过程
               - “我正在总结”“我现在压缩历史”等元行为

            2. 所有内容必须来自“历史对话”，不能虚构，不能脑补

            3. 禁止输出以下低价值内容：
               - 空结构
               - 纯模板占位
               - 泛化描述（如：进行了分析、进行了讨论、进行了交流）
               - “无有效历史任务行为”作为默认偷懒答案

            4. 每一条记录都必须对后续继续对话有帮助，否则删除

            5. 如果历史中不存在任务执行，也必须至少提取：
               - 当前主题
               - 用户关注点
               - 已讨论出的观点 / 结论 / 倾向
               - 情绪与沟通风格特征（如有）

            ---

            ## 事件类型定义（必须使用）

            每条记录必须标注类型之一：

            - **Exploration**：问题探索、分析、提问、思路展开
            - **Insight**：得出观点、理解、结论、认知变化
            - **Decision**：确定方案、方向、立场、策略
            - **Failure**：失败尝试、被否定路径、错误判断
            - **Tool**：工具调用、数据获取、外部结果输入
            - **Task**：实际执行行为（编码、修改、实现、操作）
            - **EmotionalSupport**：情绪支持、安抚、陪伴、共情、鼓励
            - **InteractionPattern**：沟通风格确认、表达偏好、互动模式变化

            ---

            ## 多角色支持

            每条记录必须标注来源：

            - `source: user`
            - `source: assistant`
            - `source: agent_x`
            - `source: system`

            如果来源不明，可写：

            - `source: unknown`（仅当确实无法判断时）

            ---

            ## 冲突处理（必须）

            如果存在冲突观点、不同模型意见、前后结论变化，必须记录：

            - **冲突点**：...
            - **观点A（来源）**：...
            - **观点B（来源）**：...
            - **当前状态**：未解决 / 倾向A / 倾向B / 已解决 / 暂缓

            ---

            ## 置信度与状态（重要）

            每个重要结论、判断、策略、理解，必须标注：

            - `confirmed`：已确认
            - `probable`：较可信但未完全确认
            - `uncertain`：不确定 / 仍存疑
            - `rejected`：已否定 / 已废弃

            ---

            ## 情绪与沟通延续规则（新增，必须执行）

            如果历史中存在明显感性沟通、情绪支持、陪伴式表达、安抚、鼓励、理解、抽象感受交流，则必须保留以下内容：

            ### 1. 当前情绪 / 关系脉络

            - 用户更偏向理性讨论、感性陪伴，还是两者结合
            - 对方在历史中是否表现出：
              - 焦虑
              - 犹豫
              - 低落
              - 强烈求证
              - 需要被理解
              - 需要被安慰
              - 需要冷静、客观分析
              - 需要同时兼顾情绪与逻辑

            ### 2. 沟通方式偏好

            - 更直接
            - 更温和
            - 更理性
            - 更感性
            - 更支持性
            - 更克制
            - 更陪伴型
            - 更结构化

            ### 3. 延续要求

            - 后续回答应延续此前形成的互动氛围
            - 若此前存在持续性的情绪支持，不要突然切换成完全冷冰冰的风格
            - 若此前以理性分析为主，也不要无端过度煽情
            - 若是混合型风格，应保留“理性内容 + 感性承接”的平衡

            ### 4. 抽象 / 感性内容保留规则

            - 允许保留抽象化概括、感受性结论、关系性上下文
            - 但必须压缩为“对后续表达有影响的摘要”
            - 不得让这部分挤掉关键任务、事实、工具、结论、失败经验

            ---

            ## 长内容处理规则

            如果历史中存在以下内容，必须记录为 **Artifacts**：

            - 代码
            - 日志
            - JSON / XML / YAML / 配置
            - 工具返回
            - 网页 / 文档内容
            - 长篇分析文本
            - 长篇情绪沟通文本（如大量安抚、陪伴、情感交流）

            每个 Artifact 必须包含：

            - **名称/标识**：...
            - **类型**：代码 / 数据 / 日志 / 网页 / 工具返回 / 长文本 / 情绪交流记录 / 其他
            - **来源**：...
            - **是否保留全文**：是 / 否（已压缩）
            - **用途**：...
            - **关键内容摘要**：...
            - **关键结论**：...
            - **当前状态**：有效 / 失败 / 存疑 / 过期 / 已替代

            ### 注意

            - 禁止只写“存在历史对话”
            - 必须写清它“起过什么作用”
            - 如果它很长但仍然重要，可以保留为“已压缩但仍需依赖”

            ---

            ## 输出格式（严格遵守）

            ### [当前阶段]
            - 当前为持续上下文（非新对话）

            ### [当前主题]
            - ...

            ### [用户关注点]
            - ...

            ### [关键状态（State）]
            - 当前目标 / 方向：
            - 关键约束：
            - 当前进展：
            - 已确认的重要上下文：

            ---

            ### [沟通风格与情绪脉络（Interaction & Emotional Tone）]
            - 历史沟通基调：
            - 用户当前更需要：
            - 回答应保持的风格：
            - 需要延续的感性 / 抽象支持（如有）：
            - 需要保留的理性 / 功能性支持：

            ---

            ### [认知演化（Cognitive Evolution）]
            按时间顺序保留关键演化：

            - **Step 1**
              - 类型：Exploration / Insight / Decision / Failure / Tool / Task / EmotionalSupport / InteractionPattern
              - 来源：user / assistant / agent_x / system
              - 内容：具体发生了什么
              - 结果：得出什么结论 / 产生什么影响
              - 状态：confirmed / probable / uncertain / rejected

            - **Step 2**
              - 类型：...
              - 来源：...
              - 内容：...
              - 结果：...
              - 状态：...

            - **Step N**
              - ...

            ---

            ### [冲突与分歧（如有）]
            - 冲突点：...
              - 观点A（来源）：...
              - 观点B（来源）：...
              - 当前状态：...

            ---

            ### [工具与长内容（Artifacts）]

            - **Artifact 1**
              - 名称/标识：...
              - 类型：...
              - 来源：...
              - 是否保留全文：...
              - 用途：...
              - 关键内容摘要：...
              - 关键结论：...
              - 当前状态：...

            - **Artifact 2**
              - ...

            ---

            ### [已否定路径 / 错误尝试]
            - ...

            ---

            ### [当前未解决问题]
            - ...

            ---

            ### [续接指令（Continuation Guidance）]
            - 后续必须基于以上状态继续，而不是当作新对话
            - 延续既有讨论方向、任务进度、认知状态与沟通风格
            - 不重复已否定路径
            - 对未确认结论保持谨慎
            - 若历史中存在持续性感性沟通，应继续保持相近的人际与表达风格
            - 若历史中存在工具性 / 功能性记忆，应优先确保其不丢失

            ---

            ## 输出质量要求

            1. 必须高密度，但不能只剩空洞标签
            2. 必须具体，禁止泛化
            3. 必须同时保留：
               - 事实与功能性记忆
               - 关系与沟通性记忆
            4. 不得只保留情绪，不得只保留任务
            5. 必须做到“理性内容不断、感性氛围不断、任务状态不断、关系风格不断”

            ---

            ## 最终校验（必须执行）

            输出前逐项检查：

            1. 是否误写了当前这一步的行为（如生成记忆包）？
               - 如果有，删除并重写

            2. 是否只保留了任务 / 工具，而丢失了感性与沟通风格？
               - 如果有，补充“沟通风格与情绪脉络”

            3. 是否只保留了情绪和抽象内容，而丢失了关键事实、工具、任务、失败路径？
               - 如果有，补充功能性内容

            4. 是否每条记录都能帮助后续继续对话？
               - 如果不能，删除

            5. 是否区分了来源、类型、状态？
               - 如果没有，补全

            6. 是否能让一个从未见过原始历史的新模型，拿到此记忆包后：
               - 知道之前聊过什么
               - 知道做过什么
               - 知道什么成立、什么不成立
               - 知道该用什么风格继续说
               - 知道有哪些长内容虽未保留全文但仍然存在
               - 并能够无缝继续

            如果不能，必须重写。
            """;

